// Multiply Two Floating-Point Numbers

#include <iostream>
using namespace std;

int main() {
    float num1, num2, product;

    cout << "Enter first floating-point number: ";
    cin >> num1;

    cout << "Enter second floating-point number: ";
    cin >> num2;

    product = num1 * num2;

    cout << "Product: " << product << endl;

    return 0;
}
